"""
组包(Packing)：将多个值合并金一个容器
解包(Unpacking)：将容器解开将其中的元素成独立的元素，分别赋值给多个变量
"""
# 利用解包组包交换变量的值
a = 11
b = 22

a, b = b, a  # todo:这就是组包加解包，右边组包成元组，在解包赋值给左边的两个变量

"""
上面的代码相当于
tup = (a, b)
a, b = tup
"""

print("交换值后a的值是：", a, " b的值是：", b)

x = 111
y = 222
z = 333

z, y, x = x, y, z

print(f"交换值后x的值是{x} y的值是{y} z的值是{z}")
